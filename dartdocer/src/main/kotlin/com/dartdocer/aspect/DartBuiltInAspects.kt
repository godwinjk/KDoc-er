/** Singleton pattern (name ends with Singleton, or has factory constructor). */
object DartSingletonAspect : DartAspect {
    override val id = "singleton"
    override val defaultNote = "Singleton — a single shared instance."

    override fun matches(element: PsiElement): Boolean {
        if (element !is DartClassDefinition) return false
        val name = element.name ?: return false
        if (name.endsWith("Singleton")) return true
        // Check for factory constructor pattern with _instance field
        val classMembers = element.classBody?.classMembers ?: return false
        val hasPrivateInstance = classMembers.varDeclarationListList.any { varDecl ->
            val varName = varDecl.varAccessDeclaration?.componentName?.text
            varName == "_instance" || varName == "_${name.replaceFirstChar { c -> c.lowercase() }}"
        }
        val hasFactory = classMembers.factoryConstructorDeclarationList.isNotEmpty()
        return hasPrivateInstance && hasFactory
    }
}
